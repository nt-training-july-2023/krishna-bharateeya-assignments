package basics_Of_Java;

public class Program8_Increment_Decrement {
	
	public static void main(String args[]) {
		
		int a =10;
		System.out.println("Current value of A :"+a);
		
		a++;
		
//		System.out.println("After ++a :"+(++a));
		System.out.println("After ++a :"+a);
		
		a++;
		System.out.println("After a++ :"+a);
		
		--a;
		System.out.println("After --a :"+a);
		
		a--;
		System.out.println("After a-- :"+a);
		
	}

}
